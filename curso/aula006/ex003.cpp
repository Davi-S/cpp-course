// Make a program that read two numbers and print their sum
#include <iostream>
using namespace std;

int main()
{   
    int n1, n2;
    cout << "Type the first number:\n$ ";
    cin >> n1;
    cout << "Type the second number:\n$ ";
    cin >> n2;
    cout << "The sum of " << n1 << " and " << n2 << " is " << n1 + n2;
    return 0;
}